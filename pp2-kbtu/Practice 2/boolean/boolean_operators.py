# 1. and — екі шарт та True болса
a = 10
b = 5
print(a > 0 and b > 0)   # True


# 2. or — бір шарт True болса жеткілікті
x = 3
y = -2
print(x > 0 or y > 0)    # True


# 3. not — керісінше мән
is_raining = False
print(not is_raining)   # True


# 4. and + comparison
age = 20
print(age >= 18 and age <= 25)   # True


# 5. or + string comparison
name = "Ali"
print(name == "Ali" or name == "John")   # True
